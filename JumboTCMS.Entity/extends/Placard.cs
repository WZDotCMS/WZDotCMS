using System;
namespace JumboTCMS.Entity
{
    /// <summary>
    /// 公告-------表映射实体
    /// </summary>

    public class Extends_Placard
    {
        public Extends_Placard()
        { }
        public Extends_Placard(
            string id,
            string title,
            string content,
            DateTime addtime,
            int state
            )
        {
            this._id = id;
            this._title = title;
            this._content = content;
            this._addtime = addtime;
            this._state = state;
        }
        private string _id;
        private string _title;
        private string _content;
        private DateTime _addtime;
        private int _state;
        /// <summary>
        /// 
        /// </summary>
        public string Id
        {
            set { _id = value; }
            get { return _id; }
        }
        /// <summary>
        /// 
        /// </summary>
        public string Title
        {
            set { _title = value; }
            get { return _title; }
        }
        /// <summary>
        /// 
        /// </summary>
        public string Content
        {
            set { _content = value; }
            get { return _content; }
        }
        /// <summary>
        /// 
        /// </summary>
        public DateTime AddTime
        {
            set { _addtime = value; }
            get { return _addtime; }
        }
        /// <summary>
        /// 
        /// </summary>
        public int State
        {
            set { _state = value; }
            get { return _state; }
        }


    }
}

